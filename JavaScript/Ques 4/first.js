
// Declaring a variable and assign a string value to it
var myString = "This is a JavaScript example";

// Check if the string contains the word "Script"
var containsScript = myString.includes("Script");

// Display the result
if (containsScript) {
  console.log("The string contains the word 'Script'.");
} 
else {
  console.log("The string does not contain the word 'Script'.");
}
